# 문법-데이터타입-문자열 표현 

# 문자열 큰 따옴표로 둘러주기
print("Hello world")
# 문자열 작은 따옴표로 둘러주기
print('python is...')

# 따옴표 문자열에 포함시키기
print("hell'o'world")

print('hell"o"world')
#escape
print("hell'o'\"w\"orld")

# 여러 줄로 출력하기
print("hello\nworld\n!!!!")

# docstring
print('''
hello
world
!!!!
''')